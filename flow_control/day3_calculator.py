# –°–æ–∑–¥–∞–π –ø—Ä–æ—Å—Ç–æ–π –∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä, –∫–æ—Ç–æ—Ä—ã–π:

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –¥–≤–∞ —á–∏—Å–ª–∞
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –æ–ø–µ—Ä–∞—Ü–∏—é (+, -, *, /)
# –í—ã–ø–æ–ª–Ω—è–µ—Ç –æ–ø–µ—Ä–∞—Ü–∏—é –∏ –≤—ã–≤–æ–¥–∏—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç
# –û—Å–æ–±–æ–µ –≤–Ω–∏–º–∞–Ω–∏–µ ‚Äî –ø—Ä–∏ –¥–µ–ª–µ–Ω–∏–∏:
# –ï—Å–ª–∏ –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ 0, –≤—ã–≤–µ–¥–∏ "–û—à–∏–±–∫–∞: –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –Ω–æ–ª—å!"
# –ò–Ω–∞—á–µ ‚Äî –≤—ã–≤–µ–¥–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
# üí° –ò—Å–ø–æ–ª—å–∑—É–π if/elif/else –¥–ª—è –≤—ã–±–æ—Ä–∞ –æ–ø–µ—Ä–∞—Ü–∏–∏. 

a = int(input("–í–≤–µ–¥–∏—Ç–µ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ: "))
b = int(input("–í–≤–µ–¥–∏—Ç–µ –≤—Ç—Ä–æ–µ —á–∏—Å–ª–æ: "))
operations = str(input("–ö–∞–∫—É—é –≤—ã–ø–æ–ª–Ω–∏—Ç—å –æ–ø–µ—Ä–∞—Ü–∏—é?(+, -, *, /) "))

if operations == "+":
    print(a + b)
elif operations == "-":
    print(a - b)
elif operations == "*":
    print(a * b)
elif operations == "/":
    if b == 0:
        print("–û—à–∏–±–∫–∞: –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –Ω–æ–ª—å!")
    else:
        print(a / b)
else:
    print("–Ø –∑–Ω–∞—é —Ç–æ–ª—å–∫–æ —ç—Ç–∏ –æ–ø–µ—Ä–∞—Ü–∏–∏: +, -, *, /")
