# –ó–∞–¥–∞—á–∞ 4 (–ø–æ –∂–µ–ª–∞–Ω–∏—é): –®–∏—Ñ—Ä –¶–µ–∑–∞—Ä—è (—É–ø—Ä–æ—â—ë–Ω–Ω—ã–π)
# –†–µ–∞–ª–∏–∑—É–π —Ñ—É–Ω–∫—Ü–∏—é caesar_encode(text, shift), –∫–æ—Ç–æ—Ä–∞—è —Å–¥–≤–∏–≥–∞–µ—Ç —Ç–æ–ª—å–∫–æ —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã –ª–∞—Ç–∏–Ω—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞ –Ω–∞ shift –ø–æ–∑–∏—Ü–∏–π –≤–ø–µ—Ä—ë–¥.

# –ü—Ä–∏–º–µ—Ä:
# caesar_encode("hello", 3) ‚Üí "khoor"

# üí° –ò—Å–ø–æ–ª—å–∑—É–π ord('a') = 97, chr(97) = 'a', –∏ –º–æ–¥—É–ª—å–Ω—É—é –∞—Ä–∏—Ñ–º–µ—Ç–∏–∫—É:
# (ord(c) - ord('a') + shift) % 26 + ord('a')


text = input("–ß—Ç–æ —Ö–æ—Ç–∏—Ç–µ –∑–∞–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å? ")
shift = int(input("–ö–∞–∫–æ–π –±—É–¥–µ—Ç —Å–¥–≤–∏–≥? "))
# –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º —Å–¥–≤–∏–≥, –≥–¥–µ 33 –∫–æ–ª-–≤–æ –±—É–∫–≤ –∞–ª—Ñ–∞–≤–∏—Ç–µ
shift = shift % 33

result = ""
for char in text:
    if "–∞" <= char <= "—è":  # —Ç–æ–ª—å–∫–æ —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã
        # 1. –ù–∞—Ö–æ–¥–∏–º –ø–æ–∑–∏—Ü–∏—é –±—É–∫–≤—ã –æ—Ç 0 –¥–æ 33
        index = ord(char) - ord("–∞")
        # 2. –°–¥–≤–∏–≥–∞–µ–º –∏ "–∑–∞–≤–æ—Ä–∞—á–∏–≤–∞–µ–º" –≤ –ø—Ä–µ–¥–µ–ª—ã 0‚Äì33
        new_index = (index + shift) % 33
        # 3. –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å–∏–º–≤–æ–ª
        new_char = chr(new_index + ord("–∞"))
        result += new_char
    else:
        # –í—Å—ë –æ—Å—Ç–∞–ª—å–Ω–æ–µ (–∑–∞–≥–ª–∞–≤–Ω—ã–µ, —Ü–∏—Ñ—Ä—ã, –ø—Ä–æ–±–µ–ª—ã, –∑–Ω–∞–∫–∏) ‚Äî –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π
        result += char

print("–ó–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç:", result)
