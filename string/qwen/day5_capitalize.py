# –ù–∞–ø–∏—à–∏ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è:

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å—Ç—Ä–æ–∫—É (–Ω–∞–ø—Ä–∏–º–µ—Ä, "–ø—Ä–∏–≤–µ—Ç, –º–∏—Ä!")
# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –µ—ë —Ç–∞–∫, —á—Ç–æ–±—ã –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –Ω–∞—á–∏–Ω–∞–ª–æ—Å—å —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã, –∞ –æ—Å—Ç–∞–ª—å–Ω—ã–µ ‚Äî —Å—Ç—Ä–æ—á–Ω—ã–µ
# (—Ä–µ–∑—É–ª—å—Ç–∞—Ç: "–ü—Ä–∏–≤–µ—Ç, –ú–∏—Ä!")
# üí° –ò—Å–ø–æ–ª—å–∑—É–π .title() ‚Äî –Ω–æ –±—É–¥—å –æ—Å—Ç–æ—Ä–æ–∂–Ω–∞: "it's" ‚Üí "It'S" üòÖ
# ‚Üí –î–ª—è —Ç–æ—á–Ω–æ–≥–æ –∫–æ–Ω—Ç—Ä–æ–ª—è –ª—É—á—à–µ —Ä–∞–∑–±–∏—Ç—å .split(), –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –∏ —Å–æ–±—Ä–∞—Ç—å .join().

# üëâ –ü—Ä–æ–¥–≤–∏–Ω—É—Ç—ã–π –≤–∞—Ä–∏–∞–Ω—Ç: –Ω–µ —Ç—Ä–æ–≥–∞—Ç—å –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è, —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã.


text = input("–í–≤–µ–¥–∏—Ç–µ —Å—Ç—Ä–æ–∫—É: ")
words = text.split()
capitalized_words = []

for word in words:
    capitalized_word = word.capitalize()
    capitalized_words.append(capitalized_word)

result = " ".join(capitalized_words)
print(result)
