#Приведение типов
#Тип int
x = int(1)
y = int(1.5)
z = int('5')
print(z)

#Тип float
x = float(1)
y = float(1.5)
z = float('5.5')
a = float('5')
print(a)

#Тип string
x = str(1)
y = str(1.5)
z = str('5.5')
print(z)
user_data_string = 'Anastasya ' + x
print(user_data_string)

#Тип Boolean
x = bool(1) #Любая строка True - кроме пустых (False), любое число True - кроме 0 (False)
y = bool("1.5")
z = bool('')
a = bool(0)
b = bool(1)
print(b)

user_data = 'Anastasya ' + str(18)

print(user_data)
